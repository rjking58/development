#or
print( "or:",bin(3) ,"|",bin(5),"=",bin(3|5))

#xor

print( "xor:",bin(3) ,"^",bin(5),"=",bin(3^5))

#twos compl
print( "twos compl: ~",bin(3),"=",bin(~3))

#left shift
print( "left shift: ",bin(5),"<< 2","=",bin(5<<2))

#right shift
print( "left shift: ",bin(5),">> 2","=",bin(5>>2))

